package model.bean;

/**
 *
 * @author Mathias
 */
public class DadosKafer {
    private String millis;
    private String erro;
    private String Kp, Kd, Ki;

    public String getMillis() {
        return millis;
    }

    public void setMillis(String millis) {
        this.millis = millis;
    }

    public String getErro() {
        return erro;
    }

    public void setErro(String erro) {
        this.erro = erro;
    }

    public String getKp() {
        return Kp;
    }

    public void setKp(String Kp) {
        this.Kp = Kp;
    }

    public String getKd() {
        return Kd;
    }

    public void setKd(String Kd) {
        this.Kd = Kd;
    }

    public String getKi() {
        return Ki;
    }

    public void setKi(String Ki) {
        this.Ki = Ki;
    }
    
}
